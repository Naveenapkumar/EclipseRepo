package controlstatements;

public class greatestamongthreenums {
    public static void main(String[] args) {
        int b = 50, c = 20, d = 10;
        if (b > c && b > d) {
            System.out.println("b is greater");
        } else if (c > b && c > d) {
            System.out.println("c is greater");
        } else {
            System.out.println("d is greater");
        
    }
}
}
